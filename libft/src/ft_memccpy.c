#include "libft.h"

void	*ft_memccpy(void *s1, const void *s2, int c, size_t n)
{
	size_t	i;
	char	*str1;
	char	*str2;

	i = 0;
	str1 = (char *)s1;
	str2 = (char *)s2;
	while (i < n)
	{
		str1[i] = str2[i];
		if ((unsigned char)str2[i] == (unsigned char)c)
			return ((char *)str1 + i + 1);
		i++;
	}
	return (NULL);
}
